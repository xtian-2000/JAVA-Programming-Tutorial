package tutorial;

public class U_math_class_methods {
	public static void main(String[] args) {
		System.out.println("Math absolute");		
		System.out.println(Math.abs(-26.7));		// Outputs the absolute value
		
		System.out.println("Math ceiling");		
		System.out.println(Math.ceil(26.7));		// Rounds up the value
		
		System.out.println("Math floor");		
		System.out.println(Math.floor(26.7));		// Rounds down the value
		
		System.out.println("Math maximum");		
		System.out.println(Math.max(26.7, 78.3));	// Outputs the highest value
		
		System.out.println("Math minimum");		
		System.out.println(Math.min(26.7, 78.3));	// Outputs the lowest value
		
		System.out.println("Math power");		
		System.out.println(Math.pow(5, 3));			// Outputs the value raised to the power of n
		

		System.out.println("Math square root");		
		System.out.println(Math.sqrt(9));			// Outputs the square root of n
	}
}
